/*************************************************************************************** 
 *
 * 快排后去对应位置元素。
 * 时间复杂度O(N*logN)，空间复杂度O(1)。
 *
 ***************************************************************************************/
class Solution {
public:
    int findKthLargest(vector<int>& nums, int k) {
        std::sort(nums.begin(), nums.end());
        return nums[nums.size() - k];
    }
};
